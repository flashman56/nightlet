const liveTime = document.querySelector("#liveTime");
const roundBadge = document.querySelector("#roundBadge");
const softStatus = document.querySelector("#softStatus");
const cardType = document.querySelector("#cardType");
const cardText = document.querySelector("#cardText");
const answerHints = document.querySelector("#answerHints");
const resultCard = document.querySelector("#resultCard");
const resultTitle = document.querySelector("#resultTitle");
const resultText = document.querySelector("#resultText");
const drawButton = document.querySelector("#drawCard");
const completeButton = document.querySelector("#completeCard");
const restartButton = document.querySelector("#restartGame");
const moodInputs = document.querySelectorAll('input[name="mood"]');

let round = 1;
let usedCards = new Set();

function updateLiveTime() {
  if (!liveTime) {
    return;
  }

  const now = new Date();
  const hours = String(now.getHours()).padStart(2, "0");
  const minutes = String(now.getMinutes()).padStart(2, "0");
  liveTime.textContent = `${hours}:${minutes}`;
}

function getMood() {
  const checked = document.querySelector('input[name="mood"]:checked');
  return checked ? checked.value : "soda";
}

function getAllCards() {
  return Object.values(cardPools).flat();
}

function getActivePool() {
  const mood = getMood();
  return mood === randomMode ? getAllCards() : cardPools[mood];
}

function pickCard() {
  const pool = getActivePool();
  if (usedCards.size >= pool.length) {
    usedCards.clear();
  }

  const availableCards = pool.filter((card) => !usedCards.has(card.key));
  const choices = availableCards.length > 0 ? availableCards : pool;
  const selected = choices[Math.floor(Math.random() * choices.length)];
  usedCards.add(selected.key);
  return selected;
}

function renderCard(card) {
  cardType.textContent = card.type;
  cardText.textContent = card.text;
  answerHints.replaceChildren(
    ...card.hints.map((hint) => {
      const item = document.createElement("span");
      item.textContent = hint;
      return item;
    }),
  );
}

function drawCard() {
  renderCard(pickCard());
}

function updateRoundUI() {
  roundBadge.textContent = `第 ${Math.min(round, roundTotal)} / ${roundTotal} 张`;
  const statusText = ["夜风刚起", "纸牌翻面", "话题亮了一下", "可以收尾了"];
  softStatus.textContent = statusText[Math.min(round - 1, statusText.length - 1)];
}

function completeCard() {
  if (round < roundTotal) {
    round += 1;
    updateRoundUI();
    drawCard();
    return;
  }

  const mood = getMood();
  resultTitle.textContent = results[mood].title;
  resultText.textContent = results[mood].text;
  resultCard.hidden = false;
  completeButton.disabled = true;
  drawButton.disabled = true;
  softStatus.textContent = "小结已收好";
}

function restartGame() {
  round = 1;
  usedCards = new Set();
  updateRoundUI();
  drawCard();
  resultCard.hidden = true;
  completeButton.disabled = false;
  drawButton.disabled = false;
}

updateLiveTime();
setInterval(updateLiveTime, 15000);

drawButton.addEventListener("click", drawCard);
completeButton.addEventListener("click", completeCard);
restartButton.addEventListener("click", restartGame);

moodInputs.forEach((input) => {
  input.addEventListener("change", restartGame);
});

restartGame();
